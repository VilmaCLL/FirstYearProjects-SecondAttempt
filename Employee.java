public class Employee {
		
	private int idNumber = 0;
	private String emploName = "";
	private String emploSurname = "";
	private int age = 0;
	private String department = "";
	
	
		public int getIdNumber() {
			return idNumber;
		}
		public void setIdNumber(int idNumber) {
			this.idNumber = idNumber;
		}
		public String getEmploName() {
			return emploName;
		}
		public void setEmploName(String emploName) {
			this.emploName = emploName;
		}
		public String getEmploSurname() {
			return emploSurname;
		}
		public void setEmploSurname(String emploSurname) {
			this.emploSurname = emploSurname;
		}
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
		public String getDepartment() {
			return department;
		}
		public void setDepartment(String department) {
			this.department = department;
		}
	
		
		
		
}
